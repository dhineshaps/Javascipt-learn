//Numbers

console.log(Math.PI); //3.14159...

console.log(Math.trunc(Math.PI)); ///Truncation -> 3

console.log(Math.round(3.5)); //round value above .5 is ipper end

//celi -> always upper end

console.log(Math.ceil(3.2)); //4

//floor -> always lower end

console.log(Math.floor(3.9)); //3

console.log(Math.pow(2, 4)); //power, exponential , 2 to the power of 4 ->16

//min

console.log(Math.min(2, 4, 5)); //2

console.log(Math.max(2, 4, 5)); //5

//random

console.log(Math.random()); //always return btw 0 to below 1

//floor is prefered to generate the sequence
console.log(Math.floor(Math.random() * 10) + 1);
